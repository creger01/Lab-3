import turtle
def color_turtle(paint):
    fred = turtle.Turtle()
    fred.color(paint)
    fred.forward(100)
    fred.right(90)
    fred.forward(100)
    fred.right(90)
    fred.forward(100)
    fred.right(90)
    fred.forward(100)


color_turtle('blue')
color_turtle('green')
color_turtle('gold')
